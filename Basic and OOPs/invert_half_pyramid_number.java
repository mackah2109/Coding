import java.util.*;

public class invert_half_pyramid_number {
    public static void main(String args[]){
        for (int i=1;i<=5;i++){
            for (int j=1;j<=5-i+1;j++){//n-i+1
                System.out.print(j);
            }
        System.out.println();
        }
    }
    
}
