import java.util.*;

public class pattern_solid_rectangle {
    public static void main(String args[]){
        //solid rectangle
        //we can take number of row and column by user
        for(int i=1;i<=4;i++){          //row
            for (int j=1;j<=4;j++){     //column
                System.out.print("*");
            }
        System.out.println();
        }
    }
    
}
